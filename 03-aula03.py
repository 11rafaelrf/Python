# Variáveis em Python
# Não ha necessidade de declará-las no início do programa
# Basta criar no meio do algoritmo com a atribuição

teste = "Rafael" # conteudo com sinal de igual(=)
print(teste) #imprime a string "Rafael"
print(type(teste)) # imprime o tipo da variável, que é float
teste = 10
print(teste) #imprime o numero 10
print(type(teste)) # imprime o tipo da variável, que é float
teste = 1.74
print(teste) #imprime o numero 1.74
print(type(teste)) # imprime o tipo da variável, que é float
teste = True
print(teste) #imprime o valor true
print(type(teste)) # imprime o tipo da variável, que é float
